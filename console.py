#!/usr/bin/python3
"""
The HBnB console.
parse(arg): makes arg readable to the interpreter.
	Arguments: arg - command line passed by user
	Return: parsed command
HBNBCommand(cmd.Cmd): inherits the cmd.Cmd class
			Defines a simple command interpreter for AirBnB_clone project.
Attributes:
	help:
		Usage: help <function> e.g help create
    		Displays the help docstring of the function
	quit(self, arg): quit from the command interpreter
	EOF(self, arg): handle EOF (^D)
	create(self, arg): creates an instance of class
	show(self, arg): Display the string representation of a class instance of a given id
	destroy(self, arg): Delete a class instance of a given id.
	all(self, arg): Display string representations of all instances of a given class.
        		If no class is specified, displays all instantiated objects.
	count(self, arg): Retrieve the number of instances of a given class.
	update(self, arg): Update a class instance of a given id by adding or updating
        		a given attribute key/value pair or dictionary.
"""
import cmd
import re
import sys
from shlex import split
from models import storage
from models.base_model import BaseModel
from models.user import User
from models.state import State
from models.city import City
from models.place import Place
from models.amenity import Amenity
from models.review import Review


def parse(arg):
    curly_braces = re.search(r"\{(.*?)\}", arg)
    brackets = re.search(r"\[(.*?)\]", arg)
    if curly_braces is None:
        if brackets is None:
            return [i.strip(",") for i in split(arg)]
        else:
            lexer = split(arg[:brackets.span()[0]])
            retl = [i.strip(",") for i in lexer]
            retl.append(brackets.group())
            return retl
    else:
        lexer = split(arg[:curly_braces.span()[0]])
        retl = [i.strip(",") for i in lexer]
        retl.append(curly_braces.group())
        return retl


class HBNBCommand(cmd.Cmd):
    """
    Defines a simple command interpreter for AirBnB_clone project.
    """

    prompt = "(hbnb) "
    __classes = {
        "BaseModel",
        "User",
        "State",
        "City",
        "Place",
        "Amenity",
        "Review"
    }

    def emptyline(self):
        '''pass if command line is empty line'''
        pass

    def default(self, arg):
        """Default behavior if command is not defined"""
        argdict = {
            "all": self.do_all,
            "show": self.do_show,
            "destroy": self.do_destroy,
            "count": self.do_count,
            "update": self.do_update
        }
        match = re.search(r"\.", arg)
        if match is not None:
            argl = [arg[:match.span()[0]], arg[match.span()[1]:]]
            match = re.search(r"\((.*?)\)", argl[1])
            if match is not None:
                command = [argl[1][:match.span()[0]], match.group()[1:-1]]
                if command[0] in argdict.keys():
                    call = "{} {}".format(argl[0], command[1])
                    return argdict[command[0]](call)
        print("*** Unknown syntax: {}".format(arg))
        return False

    def do_quit(self, arg):
        """Quit the command interpreter."""
        return True

    def do_EOF(self, arg):
        """EOF signal to exit the program."""
        return True

    def do_create(self, arg):
        """
        Create a new class instance and print its id.
        """
        argl = parse(arg)
        if len(argl) == 0:
            print("** class name missing **")
        elif argl[0] not in HBNBCommand.__classes:
            print("** class doesn't exist **")
        else:
            print(eval(argl[0])().id)
            storage.save()

    def help_create(self):
        '''help statement for create command'''
        print("\n".join(["Usage: create [class]",
            "Creates a new class instance and prints unique id",
            "Return: Nothing"]))

    def do_show(self, arg):
        """
        Display the string representation of a class instance of a given id.
        """
        argl = parse(arg)
        objdict = storage.all()
        if len(argl) == 0:
            print("** class name missing **")
        elif argl[0] not in HBNBCommand.__classes:
            print("** class doesn't exist **")
        elif len(argl) == 1:
            print("** instance id missing **")
        elif "{}.{}".format(argl[0], argl[1]) not in objdict:
            print("** no instance found **")
        else:
            print(objdict["{}.{}".format(argl[0], argl[1])])

    def help_show(self):
        '''help statement for show command'''
        print("\n".join(["Usage: show [class] [id] or [class].show([id])",
            "Displays the string representation of a class instance of a given id",
            "Return: Nothing"]))

    def do_destroy(self, arg):
        """Delete a class instance of a given id."""
        argl = parse(arg)
        objdict = storage.all()
        if len(argl) == 0:
            print("** class name missing **")
        elif argl[0] not in HBNBCommand.__classes:
            print("** class doesn't exist **")
        elif len(argl) == 1:
            print("** instance id missing **")
        elif "{}.{}".format(argl[0], argl[1]) not in objdict.keys():
            print("** no instance found **")
        else:
            del objdict["{}.{}".format(argl[0], argl[1])]
            storage.save()

    def help_destroy(self):
        '''help statement for destroy command'''
        print("\n".join(["Usage: destroy [class] [id] or [class].destroy([id])",
            "Delete a class instance of a given id",
            "Return: Nothing"]))

    def do_all(self, arg):
        """
        Display string representations of all instances of a given class.
        If no class is specified, displays all instantiated objects.
        """
        argl = parse(arg)
        if len(argl) > 0 and argl[0] not in HBNBCommand.__classes:
            print("** class doesn't exist **")
        else:
            objl = []
            for obj in storage.all().values():
                if len(argl) > 0 and argl[0] == obj.__class__.__name__:
                    objl.append(obj.__str__())
                elif len(argl) == 0:
                    objl.append(obj.__str__())
            print(objl)

    def help_all(self):
        '''help statement for all command'''
        print("\n".join(["Usage: all or all [class] or [class].all",
            "Display string representation of all instances of a given class",
            "if no class is specified, all displays instantiated objects",
            "Return: Nothing"]))

    def do_count(self, arg):
        """Retrieve the number of instances of a given class."""
        argl = parse(arg)
        count = 0
        for obj in storage.all().values():
            if argl[0] == obj.__class__.__name__:
                count += 1
        print(count)

    def help_count(self):
        '''help statement for count command'''
        print("\n".join(["Usage: count [class] or [class].count",
            "Displays the number of instances of a given class",
            "Return: Nothing"]))

    def do_update(self, arg):
        """
        Update a class instance of a given id by adding or updating
        a given attribute key/value pair or dictionary.
        """
        argl = parse(arg)
        objdict = storage.all()

        if len(argl) == 0:
            print("** class name missing **")
            return False
        if argl[0] not in HBNBCommand.__classes:
            print("** class doesn't exist **")
            return False
        if len(argl) == 1:
            print("** instance id missing **")
            return False
        if "{}.{}".format(argl[0], argl[1]) not in objdict.keys():
            print("** no instance found **")
            return False
        if len(argl) == 2:
            print("** attribute name missing **")
            return False
        if len(argl) == 3:
            try:
                type(eval(argl[2])) != dict
            except NameError:
                print("** value missing **")
                return False

        if len(argl) == 4:
            obj = objdict["{}.{}".format(argl[0], argl[1])]
            if argl[2] in obj.__class__.__dict__.keys():
                valtype = type(obj.__class__.__dict__[argl[2]])
                obj.__dict__[argl[2]] = valtype(argl[3])
            else:
                obj.__dict__[argl[2]] = argl[3]
        elif type(eval(argl[2])) == dict:
            obj = objdict["{}.{}".format(argl[0], argl[1])]
            for k, v in eval(argl[2]).items():
                if (k in obj.__class__.__dict__.keys() and
                        type(obj.__class__.__dict__[k]) in {str, int, float}):
                    valtype = type(obj.__class__.__dict__[k])
                    obj.__dict__[k] = valtype(v)
                else:
                    obj.__dict__[k] = v
        storage.save()

    def help_update(self):
        '''help statement for update command'''
        print("\n".join(["Usage: update [class] [id] [attribute_name] [attribute_value] or",
            "[class].update([id], [attribute_name, attribute_vale]) or",
            "[class].update([id], dictionary)",
            "Updates a class instance of a given unique id",
            "Return: Nothing"]))

if __name__ == "__main__":
    HBNBCommand().cmdloop()
